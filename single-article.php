            <p>Колаж – одна з найбільш популярних тенденцій цього року в графічному дизайні і в соціальних мережах, цей тренд швидко завоював популярність і в світі веб-дизайну. Змішана техніка знову в центрі уваги завдяки художникам зі світу digital і інфлюенсерам, його також взяли багато брендів і веб-дизайнери, включаючи Chobani, City Circus і Urban Outfitters.</p>
