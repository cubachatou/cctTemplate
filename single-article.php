              <p>Темний фоновий режим – виразна тенденція, яка зробила найбільший вплив на веб-дизайн. Завдяки тому, що темний режим став популярним, я вже бачила кілька сайтів, де був доданий перемикач від світлого до темного.</p>
              <cite>— Талія Джаннополас, UX-дизайнер з Envato</cite>
