              <li>застосовуйте лейбли і підказки в полях форми, а не плейсхолдери з малозрозумілими коментарями;</li>
              <li>задійте функціональні ALT-теги (альтернативні тексти) для зображень;</li>
              <li>використовуйте легкий для розуміння контент без сленгу.</li>
            </ul>

