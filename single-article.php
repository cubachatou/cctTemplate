            <p>Відео-контент сьогодні привертає інтернет-користувачів більше, ніж інші форми, не дивно, що це стало одним з популярних трендів в веб-дизайні. Моушн-дизайн і анімацію використовують всі: від батькових сайтів, таких як Alter Mind і Gridspace, до великих сервісів з пошуку роботи, таких як SEEK. Це простий спосіб зробити звичайну веб-сторінку більш цікавою.

